//Truthy vs Falsy
/*
// Truthy values are values that evaluate to true in a boolean context. They include non-zero numbers, non-empty strings, objects, and arrays.
// Falsy values are values that evaluate to false in a boolean context. They include 0, "", null, undefined, NaN, and false.
*/
if("")
{
    console.log("apple")
}
//-----------------------------------
if(0)
{
    console.log("banana")
}
else
{
    console.log("mango")
}

//----------------------------------
if(null)
{
    console.log("orange")
}
//-----------------------------------
if("jay")
{
    console.log("hello")
}
//-----------------------------------
if(undefined)
{
    console.log("world")
}
//-----------------------------------   
if(NaN)
{
    console.log("hi")
}
//-----------------------------------
if(1)
{
    console.log("bye")
}
//-----------------------------------
if(-1)
{
    console.log("hi")
}
//-----------------------------------
if(true)
{
    console.log("hello")
}